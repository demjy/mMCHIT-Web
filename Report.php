            <p>
            <strong>The</strong> mobile  Maternal  Child  Health  Information  Technology  (mMCHIT)  project  is a
              collaboration between the National Primary Health Care Development Agency (NPHCDA) and 
              Duke University, Durham, North Carolina, USA. mMCHIT  is a comprehensive set of ICT solutions 
              designed  to complement  the efforts of  the MDG supported Midwifery Service Scheme  (MSS). 
              These  ICT  solutions  will  focus  on  the  implementation  of  a  Virtual  Learning  Environment 
              dovetailing  into Decision  Support Systems and a robust Electronic Health Record System  .              </p>
            <p>              The Needs  Assessment  Exercise  was  designed  as  the  first  phase  of  the  project 
              implementation to provide the much needed guide to an implementation that is firmly consistent 
              with the local situation in the country. The expectation is that the study would educate us on the 
              ICT knowledge, attitudes, preferences and perceptions of our MCH workers. We also hoped that 
              insight gained from the study will inform our design of solutions that will not only meet the 3 
              stated goals above but will also  integrate and synergize prior ICT  investments by  the NPHCDA.</p>
            <p>              The  Exercise was  carried  out  in  6  states  spanning  the  Six Geo-Political  zones  in  the 
              country. These States include Anambra, Bauchi, Bayelsa, Ekiti, Niger, and Sokoto States. Thirty 
              Facilities were  surveyed within  the NPHCDA Cluster  system  incorporating  all  the Midwives, 
              CHEWs and a number of clients who use these facilities. The survey was done via questionnaires, 
              individual  interviews  and Focus Group discussions  to  assess  the  ICT  capacity,  readiness  and 
              acceptance among the health workers; assess existing infrastructure as well as human resource 
              capacities.</p>
            <p>              The study showed on premise that the average health worker is open to ICT in the work 
              place and that the implementation of the project would positively impact the activities of health 
              workers. There were clear ICT skill gaps identified both in the infrastructure and human resource 
              making  the need  for  training paramount  in  the scope of all  implementation.</p>
            <p>              The subsequent phases of the Project  is set  to proceed after the comprehensive survey 
              exercise with the implementation of the Virtual Learning Environment; one of the key Pillars of 
              mMCHIT. This will set  the stage  for   the successful  implementation of  the other components.<img src="images/Adobe.png" width="23" height="23" align="bottom" /> <a href="pdf/Report.zip">Download Report(pdf).</a><br />
